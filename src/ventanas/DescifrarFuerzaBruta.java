    private String descifrar() {
        char cipherText[] = TextArea_textoPlano1.getText().toCharArray();
        char planeText[] = MensajeFuerzaBruta.planeText.toCharArray();
        StringBuilder result = new StringBuilder();
        int i;
        int j;
        int intentos = 0;
        JOptionPane.showMessageDialog(this, "Longitud de la cadena: " + cipherText.length);
        for (i = 0; i < cipherText.length; i++, intentos++) {
            JOptionPane.showMessageDialog(this, "Result: " + result);
            if (MensajeFuerzaBruta.planeText.equals(result.toString())) {
                return MensajeFuerzaBruta.planeText;
            }

            for (j = 0; j < 26; j++, intentos++) {
                //JOptionPane.showMessageDialog(this, "letra antes del cambio: " + cipherText[i]);
                cipherText[i] = MonoAlphabeticCipher.changeLetterE(cipherText[i], 1);
                //JOptionPane.showMessageDialog(this, "letra despues del cambio: " + cipherText[i]);

                if (cipherText[i] == planeText[i]) {
                    result.append(cipherText[i]);
                    //JOptionPane.showMessageDialog(this, "Agrego la letra al String Builder");
                    break;
                }
            }
        }
        //return "No se pudo descifrar";
        return result.toString();
    }
